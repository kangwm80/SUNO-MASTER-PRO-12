// ──────────────────────────────────────────
// 하위 호환성을 위한 subThemes 플랫 배열 자동 생성
// lyrics.js 등에서 theme.subThemes[i]로 접근 가능
// ──────────────────────────────────────────
const SONG_THEMES = SONG_THEMES_DATA.map(theme => {
    const flatSubThemes = [];
    if (theme.ageGroups) {
        theme.ageGroups.forEach(ag => {
            if (ag.subThemes) {
                ag.subThemes.forEach(st => {
                    flatSubThemes.push(st);
                });
            }
        });
    }
    return Object.assign({}, theme, { subThemes: flatSubThemes });
});


// ──────────────────────────────────────────
// 분위기/장르 기반 주제 추천 함수
// ──────────────────────────────────────────
function recommendThemes(moods, genres, places, count) {
    const scores = {};

    SONG_THEMES.forEach(theme => {
        let score = 0;

        // 분위기 매칭
        moods.forEach(m => {
            if (theme.moods.some(tm => m.includes(tm) || tm.includes(m))) score += 5;
        });

        // 장소 매칭
        if (theme.places) {
            places.forEach(p => {
                if (theme.places.some(tp => p.includes(tp) || tp.includes(p))) score += 4;
            });
        }

        // 서브테마 키워드 매칭
        theme.subThemes.forEach(st => {
            moods.forEach(m => { if (st.keywords.some(k => m.includes(k) || k.includes(m))) score += 3; });
            places.forEach(p => { if (st.keywords.some(k => p.includes(k) || k.includes(p))) score += 2; });
        });

        // 차트 점유율 가중치
        const share = parseFloat(theme.chartShare) || 1;
        score += share * 0.5;

        // 랜덤 다양성
        score += Math.random() * 2;

        if (score > 0) scores[theme.id] = { score, theme };
    });

    return Object.values(scores)
        .sort((a, b) => b.score - a.score)
        .slice(0, count || 5)
        .map(s => s.theme);
}

// ──────────────────────────────────────────
// 주제 기반 스토리 추천
// ──────────────────────────────────────────
function getStoriesForTheme(themeId) {
    const theme = SONG_THEMES.find(t => t.id === themeId);
    if (!theme) return [];
    return theme.subThemes;
}

// ──────────────────────────────────────────
// 연령대별 서브테마 가져오기 (v5 신규)
// ──────────────────────────────────────────
function getSubThemesByAge(themeId, age) {
    const theme = SONG_THEMES.find(t => t.id === themeId);
    if (!theme || !theme.ageGroups) return [];
    const ag = theme.ageGroups.find(g => g.age === age);
    return ag ? ag.subThemes : [];
}

// ──────────────────────────────────────────
// 연령대 목록 가져오기 (v5 신규)
// ──────────────────────────────────────────
function getAgeGroupsForTheme(themeId) {
    const theme = SONG_THEMES.find(t => t.id === themeId);
    if (!theme || !theme.ageGroups) return [];
    return theme.ageGroups.map(ag => ({ age: ag.age, label: ag.label }));
}
